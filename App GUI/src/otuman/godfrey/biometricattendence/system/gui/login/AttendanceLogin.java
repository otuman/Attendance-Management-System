    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String username = loginUsernameField.getText().trim();
        char[] pass     = loginPasswordField.getPassword();
        String password = String.valueOf(pass);
        if ((!username.isEmpty()) || (!password.isEmpty())){
            try {
                boolean auth = authenticate(username, password);
                if (auth) {
                    setCurrentuser(username);
                    AttendanceMainWindow atts = new AttendanceMainWindow();
                    atts.setVisible(true);
                    this.dispose();
                }else{
                    dispStatus.setText("Wrong username or password");
                    dispStatus.setForeground(Color.red);
                }

            } catch (Exception ex) {
                Logger.getLogger(AttendanceLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
          dispStatus.setText("Type in username and password to continue");
          dispStatus.setForeground(Color.red);
        
        }


    }//GEN-LAST:event_loginButtonActionPerformed

