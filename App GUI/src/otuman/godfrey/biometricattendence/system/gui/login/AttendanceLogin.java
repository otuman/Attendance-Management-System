    public boolean authenticate(String username, String password) throws Exception {
        boolean authenticated = false;

        Controller co = new Controller();
        ProcessMD5 pMD5 = new ProcessMD5();
        String pass = pMD5.StringToMD5(password);
        co.connect();
        co.loadInstructorInfo();
        co.getInstructors();
        for (int i = 0; i < co.getInstructors().size(); i++) {
            Instructor inst = co.getInstructors().get(i);
            if (username.equals(inst.getUsername()) && pass.equals(inst.getPassword())) {
                authenticated = true;
            }
        }
        return authenticated;
    }
    
   

